# Aurum Circuit – Suit System Overview

The Aurum Circuit is built on four primary Suits.  
Each Suit represents a functional domain of human support, forming a complete bio-digital loop when combined.

---

## ♡ Heart Suit – Health & Regulation
Purpose: Stabilise and protect internal balance.

Focus Areas:
• temperature regulation  
• pain reduction  
• circulation support  
• healing assistance  
• safety feedback (early alerts)

---

## ♠ Spade Suit – Strength & Build
Purpose: Assist with load, structure, and physical stability.

Focus Areas:
• lifting support  
• posture alignment  
• fatigue reduction  
• muscular reinforcement  
• industrial safety and labour protection

---

## ♣ Club Suit – Protection & Shielding
Purpose: Create a buffer between the user and environmental hazards.

Focus Areas:
• impact absorption  
• pressure shielding  
• fall protection  
• emergency resilience  
• hazard resistance (non-weaponised)

---

## ♦ Diamond Suit – Mobility & Flow
Purpose: Improve smoothness, movement quality, and energy efficiency.

Focus Areas:
• balance assistance  
• gait stabilisation  
• reduced strain  
• continuous motion flow  
• everyday mobility support

---

## Unified Aurum Circuit
When combined, the four Suits form a single human-first exo-skin:

• Heart keeps the body regulated  
• Spade carries the load  
• Club protects the form  
• Diamond enables motion  

Together, they create the **Aurum Circuit** — a supportive, non-weaponised system that enhances life without replacing autonomy.